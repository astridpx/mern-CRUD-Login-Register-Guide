const mongoose = require("mongoose");

// const schema = mongoose.Schema;

// Schema for database
const accountSchema = new mongoose.Schema(
  {
    fullname: {
      type: String,
      required: true,
      trim: true,
    },
    username: {
      type: String,
      required: true,
      trim: true,
    },
    age: {
      type: Number,
      required: true,
      trim: true,
    },
    password: {
      type: String,
      required: true,
      trim: true,
    },
  },
  {
    timestamps: true,
  }
);

// supply the name of collection and the schema
const Person = mongoose.model("accounts", accountSchema);

module.exports = Person;
